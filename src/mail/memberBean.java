package mail;

public class memberBean {
	private String id;
	private String email;
	private String injeng;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getInjeng() {
		return injeng;
	}
	public void setInjeng(String injeng) {
		this.injeng = injeng;
	}
	
}
